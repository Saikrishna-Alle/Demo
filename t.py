x = int(input("Enter a number: "))
y = int(input("Enter another number: "))

def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide (x, y):
    return x / y

print(subtract(x, y))
print(add(x, y))
print(multiply(x, y))
print(divide(x, y))
